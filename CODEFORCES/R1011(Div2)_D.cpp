void solve() {
	int t; cin >> t;
	while(t--) {
		int n, k; cin >> n >> k;
		ll kq = 0;
		priority_queue<int> pq;
		for (int i = 1; i <= n; i++) {
			int d; cin >> d;
			pq.push(d);
			if ((n-i+1)%(k+1) == 0) {
				kq += pq.top();
				pq.pop();
			}
		}
		cout << kq << endl;
	}
}
